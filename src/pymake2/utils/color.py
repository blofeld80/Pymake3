def blue(s):
    return '\033[94m{}\033[0m'.format(s)

def green(s):
    return '\033[92m{}\033[0m'.format(s)

def red(s):
    return '\033[91m{}\033[0m'.format(s)

def yellow(s):
    return '\033[93m{}\033[0m'.format(s)
